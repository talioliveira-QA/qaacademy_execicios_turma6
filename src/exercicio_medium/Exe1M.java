    public void exibirParImpar() {
        int i=0;
        while (i<=100){
            if (i % 2==0){
                System.out.println("O numero:" +i+" é par");
            }
            else{
                System.out.println("O número:" +i+ " é impar");
            }
            i++;
        }
    }




